# Creating a set of Prime Numbers.
setPrimeNumbers = {2, 3, 5, 7}

# Prints the existing set.
print(setPrimeNumbers)

# Adding a member to the existing set 'setPrimeNumbers'. 
setPrimeNumbers.add(11)

# Prints the updated set.
print(setPrimeNumbers)

# Clearing a set using clear() method.
setPrimeNumbers.clear()

# Prints the statement after clearing the set.
print('Set(after clear):', setPrimeNumbers)
